#include <iostream>
using namespace std;


int main() {
    int a = 10;
    int b = 10;
    
    //postincrement
    cout << a << endl;
    cout << a++ << endl;
    cout << a << endl << endl;
    
    //preincrement
    cout << b << endl;
    cout << ++b << endl;
    cout << b << endl;
    
    
    return 0;
}